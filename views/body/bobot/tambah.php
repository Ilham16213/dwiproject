                    for ($i = 0; $i < $numCriteria; $i++) {
                        $sum = 0;
                        for ($j = 0; $j < $numCriteria; $j++) {
                            $sum += $pairwiseComparisonMatrix[$i][$j] * $weights[$j];
                        }
                        $sumLambdaMax += $sum / $weights[$i];
                    }
                    $lambdaMax = $sumLambdaMax / $numCriteria;

                    // Hitung CI
                    return ($lambdaMax - $numCriteria) / ($numCriteria - 1);
                }

