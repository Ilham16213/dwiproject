                        }
                        $weights[$i] = $sum / $numCriteria;
                    }

                    return $weights;
                }

                // Fungsi untuk menghitung CI (Consistency Index)
                function calculateCI($pairwiseComparisonMatrix, $weights) {
                    $numCriteria = count($pairwiseComparisonMatrix);
                    $sumLambdaMax = 0;

