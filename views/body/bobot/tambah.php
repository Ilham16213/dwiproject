                // Fungsi untuk menghitung bobot kriteria dari matriks normalisasi
                function calculateWeights($normalizedMatrix) {
                    $numCriteria = count($normalizedMatrix);
                    $weights = array_fill(0, $numCriteria, 0);

                    // Hitung rata-rata setiap baris
